    private void btnSentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSentActionPerformed
        // TODO add your handling code here:
        String sender=lbdriver.getText().toString();
      String  rgrp=jComboBox2.getSelectedItem().toString();
      String receiver=txtreceiveremail.getText().toString();
      String sgrp="Drivers";
      String sms=jTextArea2.getText().toString().trim();
      String mtype="";
         sql="Insert into message(sender_email, receiver_group, receiver_email,sender_grp,sms, timesent,type)VALUES(?,?,?,?,?,now(),?)";
         
         if(!sender.isEmpty()&&!sms.isEmpty() &&(receiver.isEmpty() ||sgrp.isEmpty()))
         {
         mtype="Gen";
         receiver="All";
         }
         else if(!sender.isEmpty() &&!sms.isEmpty() &&!receiver.isEmpty() &&!sgrp.isEmpty())
         {
         mtype="spec";
         
         }
         else if(sender.equalsIgnoreCase(receiver)&&(sms.isEmpty()&&(receiver.isEmpty()||rgrp.isEmpty()))){
             JOptionPane.showMessageDialog(this, "You need to sent sms out not to yourself or empty sms");
         }
         try{
         pst=officerconn.prepareStatement(sql);
         pst.setString(1, sender);
         pst.setString(2, rgrp);
         pst.setString(3, receiver);
         pst.setString(4, sgrp);
         pst.setString(5, sms);
         pst.setString(6, mtype);
         pst.execute();
         txtreceiveremail.setText("");
         jTextArea2.setText("");
         JOptionPane.showMessageDialog(this, "Message sent successfully");
         sentMessages();
         InboxMessages();
         }
         catch(Exception e)
         {
         JOptionPane.showMessageDialog(this, e.getMessage());
         
         }
    }//GEN-LAST:event_btnSentActionPerformed

