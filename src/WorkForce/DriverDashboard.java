    private void viewprofile() {
        String u, p, e, others, est, f, r;
        u = lbdriver.getText().toString();
        sql = "select * from driver, trucks where driver.email='" + u + "' and driver.truck_allocated=trucks.reg_number";
        try {
            pst = officerconn.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                //u=rs.getString("");
                p = rs.getString("phone_number");
                txtphone.setText(p);
                e = rs.getString("email");
                txtemail.setText(e);
                others = rs.getString("other_names");
                txtother.setText(others);
                f = rs.getString("firstname");
                txtfname.setText(f);
                est = rs.getString("truck_allocated");
                txtest.setText(est);
                r = rs.getString("router_name");
                txtroute.setText(r);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

